#include <stdio.h>
int main(){
    //integer
    int a = 10;
    printf("integer: %d\n",a);

    //double
    double b =3.433;
    printf("double: %lf\n",b);

    //float
    float c =4.685;
    printf("float: %f\n",c);

    //long int
    long int d =63.38746;
    printf("long int: %li\n", d);


    return 0;
}
   